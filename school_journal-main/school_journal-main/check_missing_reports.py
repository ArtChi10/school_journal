def norm(s: str) -> str:
    s = s.strip().lower()
    s = s.replace("ё", "е")
    s = re.sub(r"[_\-]+", " ", s)
    s = re.sub(r"\s+", " ", s)
    return s

def tokens(s: str) -> set:
    return set(norm(s).split())

def name_variants(fullname: str):
    parts = norm(fullname).split()
    if len(parts) >= 2:
        first, last = parts[0], parts[1]
        return {f"{first} {last}", f"{last} {first}"}
    return {" ".join(parts)}

def match_name_to_filename(name: str, filename_stem: str) -> bool:
    name_tok = tokens(name)
    file_tok = tokens(filename_stem)
    if name_tok and name_tok.issubset(file_tok):
        return True
    for v in name_variants(name):
        if v in norm(filename_stem):
            return True
    return False

def collect_pdf_paths(root: Path):
    return [p for p in root.rglob("*.pdf")]

def main():
    ap = argparse.ArgumentParser(description="Найти ЛИШНИЕ PDF-файлы (которых нет в списке имён).")
    ap.add_argument("--dir", default="pdf_out", help="Корневая папка с PDF (по умолчанию pdf_out)")
    ap.add_argument("--names-file", help="TXT со списком имён (по одному на строке). Если не задан — берём встроенный.")
    args = ap.parse_args()

    root = Path(args.dir).resolve()
    if not root.is_dir():
        print(f"Папка не найдена: {root}")
        return

    raw = Path(args.names_file).read_text(encoding="utf-8") if args.names_file else NAMES_RAW
    wanted = [line.strip() for line in raw.splitlines() if line.strip()]
    print(f"Имён в списке: {len(wanted)}")

    files = collect_pdf_paths(root)
    print(f"PDF найдено: {len(files)} (включая подпапки)")

    def is_in_wanted(stem: str) -> bool:
        return any(match_name_to_filename(name, stem) for name in wanted)

    extras = []
    for p in files:
        if not is_in_wanted(p.stem):
            extras.append(p)

    print("\n— ЛИШНИЕ ФАЙЛЫ (нет в списке имён) —")
    for p in extras:
        try:
            rel = p.relative_to(root)
            print(rel.with_suffix(""))  # печатаем без .pdf
        except Exception:
            print(p.with_suffix(""))
    print(f"\nИТОГО лишних: {len(extras)} из {len(files)} файлов")

if __name__ == "__main__":
    main()
